#include "libft.h"

size_t				ft_count_char(char const *s, char c)
{
	size_t			i;
	int				l;

	l = 0;
	i = 0;
	if (s[0] == '#')
		return (1);
	while (s[l] != '\0')
	{
		if (s[l] != c && s[l] != '\0')
		{
			i++;
			while (s[l] != c && s[l] != '\0')
				l++;
		}
		else
			l++;
	}
	return (i);
}
